type conditionGroup struct {
	condition
	or bool
	cs []Condition
}

func (c *conditionGroup) setNot() {
	c.not = true
	c.parenthesized = true
}

func (c *conditionGroup) setParenthesized() {
	c.parenthesized = true
}

func (c *conditionGroup) write(p *gdao.BuilderProt) bool {
	if c == nil || len(c.cs) == 0 {
		return false
	}
	var ok bool
	c.doWrite(p, func() {
		for i, cond := range c.cs {
			if i != 0 {
				if c.or {
					p.Write(" OR ")
				} else {
					p.Write(" AND ")
				}
			}
			written := cond.write(p)
			if !ok {
				ok = written
			}
		}
	})
	return ok
}

